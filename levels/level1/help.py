from aiogram import Router
from aiogram.types import Message
from aiogram.filters import Command

router = Router()

# Карта команд Первого Уровня с разделением по правам доступа
COMMANDS = {
    "/start": "Запуск бота и открытие главного меню выбора уровней",
    "/help": "Вывод списка всех доступных команд",
    "/ping": "Проверка задержки отклика и работоспособности бота",
    "/userinfo": "Вывод данных вашего профиля (ID, имя, юзернейм)",
    "/time": "Отображение текущего времени сервера",
    "/chatinfo": "Сбор технической информации о текущем чате/группе",
    "/avatar": "[Админ] Установить новую аватарку для группы",
    "/version": "Информация о текущей версии сборки",
    "/exit": "[Админ] Принудительно заставить бота покинуть группу"
}


# Хэндлер вывода справочной информации по командам бота
@router.message(Command("help"))
async def cmd_help(message: Message):
    help_text = "📋 <b>Список доступных команд Первого Уровня:</b>\n\n"
    
    # Собираем красивый маркированный список на основе словаря
    for cmd, desc in COMMANDS.items():
        help_text += f"🔹 {cmd} — <i>{desc}</i>\n"
    
    # Отправляем сформированный текст без лишних ручных дублирований строки
    await message.answer(help_text, parse_mode="HTML")